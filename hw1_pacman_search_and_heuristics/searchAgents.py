def cornersHeuristic(state, problem):
    
    # README: Thought process analysed on the pdf
    
    visited = state[1]          # A list of the visited corners
    notVisited = set()          # A set of the unvisited corners (set for O(1))
    corners = problem.corners   # The 4 corner coordinates  
    
    # Our heristic must not take into account the goals/corners that 
    # we already hit/visited so create a list only including the unvisited
    for corner in corners:
        if corner not in visited:
            notVisited.add(corner)

    node = state[0] # The coordinates of the given state
    value = 0       # The heuristic value of the state

    # While we have not covered all the available corners
    while len(notVisited)!=0:
        
        # The minimum distance from the current node (node changes
        # on every iteration) to any other node-corner.
        # I start with +inf distance to ensure any corner distance 
        # will be accepted on the first iteration
        minDist = float("inf")

        # We test all corners in the unvisited set, to find the min possible
        # distance from the current node, to any other node
        for corner in notVisited: 
            dist = util.manhattanDistance(node,corner)
            if minDist > dist:
                minDist = dist
                minCorn = corner

        value+=minDist  # We add the distance to the heuristic value
        node=minCorn    # We replace the current node with the one we found
                        # to make the path pass through all available corners
        notVisited.remove(minCorn) # Remove the corner since we just "visited" it

    return value

