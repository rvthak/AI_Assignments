def foodHeuristic(state, problem):

    # README: Pre-computation to cut down on runtime
    # See pdf for explanation and Thought process

    # If we have not yet created the dictionary entry, create it
    # and fill it with data (Runs only the first time the function runs)
    # I consider (-1,-1) the impossible node, i just use it as a flag to
    # check if the dictionary is populated with data yet
    if ((-1,-1),(-1,-1)) not in problem.heuristicInfo:

        # From the food positions, we get the height and the width of the map
        foodMap=state[1]
        width =foodMap.width
        height=foodMap.height

        # We get the wall positions to avoid unnecessary calculations
        walls = problem.walls
        
        # We calculate the maze distance from every single non-wall state
        # to every other non-wall state (We know the top and bottom rows,
        # along with the rightmost and leftmost columns are walls so we dont check them)
        for x in range(1,width-1):
            for y in range(1,height-1):
                if not walls[x][y]:
                    for x1 in range(1,width-1):
                        for y1 in range(1,height-1):
                            if not walls[x1][y1]:
                                problem.heuristicInfo[(x,y),(x1,y1)]=mazeDistance((x,y), (x1,y1), problem.startingGameState)

        # Create the dictionary entry so this data is calculated only once
        # since the walls/map do not change during execution we are safe to
        # use them for the whole problem
        problem.heuristicInfo[((-1,-1),(-1,-1))]=-1

    
    position = state[0]             # The position of the state
    foodList = state[1].asList()    # A list holding the coordinates of food on the map
    value = 0                # The heuristic value of the state

    # We check every single food available to find the food that is 
    # the furthest one away from the current state position
    # The closer we are to the all available foods, the better the state value
    for food in foodList:
        # We get the precalculated distances from our dictionary in O(1) 
        distance = problem.heuristicInfo[position,food]
        if distance > value: # We keep the longest distance
            value = distance

    return value


